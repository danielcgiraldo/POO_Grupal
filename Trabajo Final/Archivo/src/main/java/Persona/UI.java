    private TableModelListener modelListener = new TableModelListener() {
        public void tableChanged(TableModelEvent e) {
            TableModel model = (TableModel) e.getSource();
            int row = e.getFirstRow();
            Persona.delete_person(model.getValueAt(row, 0).toString());
            Persona persona = new Persona();
            persona.name = model.getValueAt(row, 1).toString();
            persona.surname = model.getValueAt(row, 2).toString();
            persona.phone = model.getValueAt(row, 3).toString();
            persona.id = model.getValueAt(row, 0).toString();
            persona.add_person();
        }
    };

    private void add_table() {
        Table.getModel().addTableModelListener(modelListener);
    }

