    private void btnAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddActionPerformed
        // ======== Create Persona ========== //
        Persona persona = new Persona();
        persona.name = txtName.getText();
        persona.surname = txtSurname.getText();
        persona.phone = txtPhone.getText();
        persona.id = txtCedula.getText();
        // ======== Data Base ====== //

        boolean flag = persona.add_person();

        if (flag) {
            DefaultTableModel model = (DefaultTableModel) Table.getModel();
            model.addRow(new Object[]{persona.id, persona.name, persona.surname, persona.phone});
            txtName.setText("");
            txtSurname.setText("");
            txtPhone.setText("");
            txtCedula.setText("");
        } else {
            JOptionPane.showMessageDialog(null,
                    "No se pudo agregar a la persona. Por favor verifica los datos ingresados.",
                    "Error agregando persona",
                    JOptionPane.ERROR_MESSAGE);
        }

    }//GEN-LAST:event_btnAddActionPerformed

    private void btnCleanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCleanActionPerformed
        DefaultTableModel model = (DefaultTableModel) Table.getModel();
        model.removeTableModelListener(modelListener);
        if (Persona.vaciar()) {
            
            for (int i = 0; i < Table.getRowCount(); i++) {
                model.removeRow(i);
                i -= 1;
            }
        } else {
            JOptionPane.showMessageDialog(null,
                    "No se pudo vaciar la tabla. Hubo un error en el sistema.",
                    "Error inesperado",
                    JOptionPane.ERROR_MESSAGE);
        }
        add_table();
    }//GEN-LAST:event_btnCleanActionPerformed

    private void btnDeleteRowActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteRowActionPerformed

        DefaultTableModel model = (DefaultTableModel) Table.getModel();
        model.removeTableModelListener(modelListener);
        for (int i = 0; i < Table.getSelectedRows().length; i++) {
            if (Persona.delete_person(model.getValueAt(Table.getSelectedRow(), 0).toString())) {
                model.removeRow(Table.getSelectedRow());
            } else {
                JOptionPane.showMessageDialog(null,
                        "No se pudo eliminar una(s) persona(s).",
                        "Error inesperado",
                        JOptionPane.ERROR_MESSAGE);
            }
        }
        add_table();
    }//GEN-LAST:event_btnDeleteRowActionPerformed

